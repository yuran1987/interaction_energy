function y=U4_molier(Z1,Z2,r,a)
e2=14.4;
R1=(exp(-((3*r)/(10*a)))*(-0.00110311*a^3-0.00141083*a^2*r+0.000175412*a*r^2-4.22051*10^(-6)*r^3))/a^3;
R2=(exp(-((1.2*r)/a))*(0.317351*a^3-0.262551*a^2*r+0.0427001*a*r^2-0.00164711*r^3))/a^3;
R3=(exp(-((6*r)/a))*(0.183752*a^3-0.0867131*a^2*r+0.00887664*a*r^2-0.000225*r^3))/a^3;

if r>0
  y=2*Z1*Z2*e2*(R1+R2+R3)/(r);
else
  y=0;
endif
endfunction






















